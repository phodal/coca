class MultipleIf {
    public static void main(String args[]) {
        int i = 20;

        if (i == 10)
            System.out.println("i is 10");
        if (i == 15)
            System.out.println("i is 15");
        if (i == 20)
            System.out.println("i is 20");
        if (i == 20)
            System.out.println("i is 20");
        if (i == 20)
            System.out.println("i is 20");
        if (i == 20)
            System.out.println("i is 20");
        if (i == 20)
            System.out.println("i is 20");
        if (i == 20)
            System.out.println("i is 20");
        else
            System.out.println("i is not present");
    }
}
